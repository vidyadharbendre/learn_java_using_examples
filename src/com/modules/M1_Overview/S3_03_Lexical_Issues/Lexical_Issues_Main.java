package com.modules.M1_Overview.S3_03_Lexical_Issues;

// Example demonstrating lexical issues in Java

public class Lexical_Issues_Main {
    public static void main(String[] args) {
        // Whitespace
        int x = 10;
        int y=20; // No whitespace between variable name and assignment

        // Identifiers
        int myVariable = 5;

        // Literals
        String message = "Hello, world!";
        double pi = 3.14;

        // Comments
        // This is a single-line comment
        /*
        This is a
        multi-line comment
        */

        // Separators
        int a = 1, b = 2; // Separated by comma

        // Java Keywords
        int if_ = 5; // Using a keyword as an identifier
    }
}

