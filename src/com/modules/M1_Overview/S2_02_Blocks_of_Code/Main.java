package com.modules.M1_Overview.S2_02_Blocks_of_Code;

// Example demonstrating blocks of code in Java

public class Main {
    public static void main(String[] args) {
        // Block 1
        {
            int x = 10;
            System.out.println("Block 1: x = " + x);
        }

        // Block 2
        {
            int x = 20; // Another variable named x
            System.out.println("Block 2: x = " + x);
        }
    }
}
// The output of the above program is shown as below
/*
Block 1: x = 10
Block 2: x = 20
 */
